// array method
var a=['Sanjay','Aman','Ram','Karan'];
document.write(a,'<br>');
// print in alphabetical order
a.sort();
document.write(a,'<br>');
// print in reverse order
a.reverse();
document.write(a,'<br>');
// delete last value in array
a.pop();
document.write(a,'<br>');
// add extra value in last position
a.push('Rahul');
document.write(a,'<br>');
// delete first value in array
a.shift();
document.write(a,'<br>');
// add extra value in first position
a.unshift('Manish');
document.write(a,'<br><br>');